import 'package:flutter/material.dart';
import 'dart:async';
import '../services/hardware_service.dart';
import '../widgets/local_ml_widget.dart';

class SimpleHomePage extends StatefulWidget {
  const SimpleHomePage({super.key});

  @override
  State<SimpleHomePage> createState() => _SimpleHomePageState();
}

class _SimpleHomePageState extends State<SimpleHomePage> {
  // Ìï†Ïùº Î™©Î°ù
  final List<TodoItem> _todos = [
    TodoItem(title: 'ÌîÑÎ°úÏ†ùÌä∏ ÌöåÏùò Ï∞∏ÏÑù', isCompleted: false, priority: 'high'),
    TodoItem(title: 'Ïö¥ÎèôÌïòÍ∏∞', isCompleted: true, priority: 'medium'),
    TodoItem(title: 'Flutter Í≥µÎ∂Ä', isCompleted: false, priority: 'high'),
    TodoItem(title: 'ÎèÖÏÑú 1ÏãúÍ∞Ñ', isCompleted: false, priority: 'low'),
  ];

  // Ìï†Ïùº Ï∂îÍ∞Ä Ïª®Ìä∏Î°§Îü¨
  final TextEditingController _todoController = TextEditingController();
  String _selectedPriority = 'medium';

  @override
  void dispose() {
    _todoController.dispose();
    super.dispose();
  }

  // Ìï†Ïùº ÌÜ†Í∏Ä
  void _toggleTodo(int index) {
    setState(() {
      _todos[index] = TodoItem(
        title: _todos[index].title,
        isCompleted: !_todos[index].isCompleted,
        priority: _todos[index].priority,
      );
    });
  }

  // Ìï†Ïùº ÏÇ≠Ï†ú
  void _deleteTodo(int index) {
    setState(() {
      _todos.removeAt(index);
    });
  }

  // Ìï†Ïùº Ï∂îÍ∞Ä
  void _addTodo() {
    if (_todoController.text.trim().isEmpty) return;
    
    setState(() {
      _todos.add(TodoItem(
        title: _todoController.text.trim(),
        isCompleted: false,
        priority: _selectedPriority,
      ));
    });
    
    _todoController.clear();
    Navigator.of(context).pop();
  }

  // Ìï†Ïùº Ï∂îÍ∞Ä Îã§Ïù¥ÏñºÎ°úÍ∑∏
  void _showAddTodoDialog() {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text('ÏÉà Ìï†Ïùº Ï∂îÍ∞Ä'),
        content: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            TextField(
              controller: _todoController,
              decoration: const InputDecoration(
                hintText: 'Ìï†ÏùºÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî',
                border: OutlineInputBorder(),
              ),
              autofocus: true,
            ),
            const SizedBox(height: 16),
            DropdownButtonFormField<String>(
              value: _selectedPriority,
              decoration: const InputDecoration(
                labelText: 'Ïö∞ÏÑ†ÏàúÏúÑ',
                border: OutlineInputBorder(),
              ),
              items: const [
                DropdownMenuItem(value: 'high', child: Text('ÎÜíÏùå')),
                DropdownMenuItem(value: 'medium', child: Text('Î≥¥ÌÜµ')),
                DropdownMenuItem(value: 'low', child: Text('ÎÇÆÏùå')),
              ],
              onChanged: (value) {
                setState(() {
                  _selectedPriority = value!;
                });
              },
            ),
          ],
        ),
        actions: [
          TextButton(
            onPressed: () => Navigator.of(context).pop(),
            child: const Text('Ï∑®ÏÜå'),
          ),
          ElevatedButton(
            onPressed: _addTodo,
            style: ElevatedButton.styleFrom(
              backgroundColor: Colors.pink.shade400,
              foregroundColor: Colors.white,
            ),
            child: const Text('Ï∂îÍ∞Ä'),
          ),
        ],
      ),
    );
  }

  Widget _buildCharacterImage() {
    int completedCount = _todos.where((todo) => todo.isCompleted).length;
    double completionRate = _todos.isEmpty ? 0 : completedCount / _todos.length;
    
    String characterEmoji;
    String statusText;
    
    if (completionRate >= 0.8) {
      characterEmoji = 'üéâ';
      statusText = 'ÏôÑÎ≤ΩÌï¥Ïöî!';
    } else if (completionRate >= 0.5) {
      characterEmoji = 'üí™';
      statusText = 'Ïó¥Ïã¨Ìûà ÌïòÍ≥† ÏûàÏñ¥Ïöî!';
    } else if (completionRate > 0) {
      characterEmoji = 'üå±';
      statusText = 'ÏãúÏûëÏù¥ Ï¢ãÏïÑÏöî!';
    } else {
      characterEmoji = 'üòä';
      statusText = 'ÏÉàÎ°úÏö¥ ÌïòÎ£®!';
    }

    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.pink.withOpacity(0.1),
            blurRadius: 15,
            offset: const Offset(0, 5),
          ),
        ],
      ),
      child: Column(
        children: [
          Text(
            characterEmoji,
            style: const TextStyle(fontSize: 80),
          ),
          const SizedBox(height: 10),
          Text(
            statusText,
            style: TextStyle(
              fontSize: 18,
              fontWeight: FontWeight.bold,
              color: Colors.pink.shade600,
            ),
          ),
          const SizedBox(height: 8),
          Text(
            'ÏôÑÎ£åÏú®: ${(completionRate * 100).toInt()}%',
            style: TextStyle(
              fontSize: 14,
              color: Colors.grey.shade600,
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildQuickStats() {
    int totalTodos = _todos.length;
    int completedTodos = _todos.where((todo) => todo.isCompleted).length;
    int pendingTodos = totalTodos - completedTodos;

    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.pink.withOpacity(0.1),
            blurRadius: 15,
            offset: const Offset(0, 5),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const Text(
            'üìä Ïò§ÎäòÏùò ÏÑ±Í≥º',
            style: TextStyle(
              fontSize: 18,
              fontWeight: FontWeight.bold,
              color: Colors.black87,
            ),
          ),
          const SizedBox(height: 15),
          Row(
            children: [
              Expanded(
                child: _buildStatCard(
                  'Ï†ÑÏ≤¥ Ìï†Ïùº',
                  totalTodos.toString(),
                  Colors.blue.shade400,
                  Icons.list_alt,
                ),
              ),
              const SizedBox(width: 12),
              Expanded(
                child: _buildStatCard(
                  'ÏôÑÎ£å',
                  completedTodos.toString(),
                  Colors.green.shade400,
                  Icons.check_circle,
                ),
              ),
              const SizedBox(width: 12),
              Expanded(
                child: _buildStatCard(
                  'ÎÇ®ÏùÄ Ìï†Ïùº',
                  pendingTodos.toString(),
                  Colors.orange.shade400,
                  Icons.pending,
                ),
              ),
            ],
          ),
        ],
      ),
    );
  }

  Widget _buildStatCard(String title, String value, Color color, IconData icon) {
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: color.withOpacity(0.1),
        borderRadius: BorderRadius.circular(12),
        border: Border.all(
          color: color.withOpacity(0.3),
          width: 1,
        ),
      ),
      child: Column(
        children: [
          Icon(icon, color: color, size: 24),
          const SizedBox(height: 8),
          Text(
            value,
            style: TextStyle(
              fontSize: 20,
              fontWeight: FontWeight.bold,
              color: color,
            ),
          ),
          const SizedBox(height: 4),
          Text(
            title,
            style: TextStyle(
              fontSize: 12,
              color: Colors.grey.shade600,
            ),
            textAlign: TextAlign.center,
          ),
        ],
      ),
    );
  }

  Widget _buildTodoManagement() {
    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.pink.withOpacity(0.1),
            blurRadius: 15,
            offset: const Offset(0, 5),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              const Text(
                '‚úÖ Ìï†Ïùº Í¥ÄÎ¶¨',
                style: TextStyle(
                  fontSize: 18,
                  fontWeight: FontWeight.bold,
                  color: Colors.black87,
                ),
              ),
              ElevatedButton.icon(
                onPressed: _showAddTodoDialog,
                icon: const Icon(Icons.add, size: 18),
                label: const Text('Ï∂îÍ∞Ä'),
                style: ElevatedButton.styleFrom(
                  backgroundColor: Colors.pink.shade400,
                  foregroundColor: Colors.white,
                  padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
                  shape: RoundedRectangleBorder(
                    borderRadius: BorderRadius.circular(20),
                  ),
                ),
              ),
            ],
          ),
          const SizedBox(height: 15),
          if (_todos.isEmpty)
            Container(
              padding: const EdgeInsets.all(20),
              child: Center(
                child: Column(
                  children: [
                    Icon(
                      Icons.task_alt,
                      size: 48,
                      color: Colors.grey.shade400,
                    ),
                    const SizedBox(height: 12),
                    Text(
                      'ÏïÑÏßÅ Ìï†ÏùºÏù¥ ÏóÜÏñ¥Ïöî!\nÏÉàÎ°úÏö¥ Ìï†ÏùºÏùÑ Ï∂îÍ∞ÄÌï¥Î≥¥ÏÑ∏Ïöî.',
                      textAlign: TextAlign.center,
                      style: TextStyle(
                        color: Colors.grey.shade600,
                        fontSize: 14,
                      ),
                    ),
                  ],
                ),
              ),
            )
          else
            ListView.builder(
              shrinkWrap: true,
              physics: const NeverScrollableScrollPhysics(),
              itemCount: _todos.length,
              itemBuilder: (context, index) {
                final todo = _todos[index];
                return _buildTodoItem(todo, index);
              },
            ),
        ],
      ),
    );
  }

  Widget _buildTodoItem(TodoItem todo, int index) {
    Color priorityColor;
    switch (todo.priority) {
      case 'high':
        priorityColor = Colors.red.shade400;
        break;
      case 'medium':
        priorityColor = Colors.orange.shade400;
        break;
      case 'low':
        priorityColor = Colors.green.shade400;
        break;
      default:
        priorityColor = Colors.grey.shade400;
    }

    return Container(
      margin: const EdgeInsets.only(bottom: 8),
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: todo.isCompleted ? Colors.grey.shade50 : Colors.white,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(
          color: todo.isCompleted ? Colors.grey.shade200 : priorityColor.withOpacity(0.3),
          width: 1,
        ),
      ),
      child: Row(
        children: [
          GestureDetector(
            onTap: () => _toggleTodo(index),
            child: Container(
              width: 24,
              height: 24,
              decoration: BoxDecoration(
                color: todo.isCompleted ? Colors.green.shade400 : Colors.transparent,
                border: Border.all(
                  color: todo.isCompleted ? Colors.green.shade400 : Colors.grey.shade400,
                  width: 2,
                ),
                borderRadius: BorderRadius.circular(6),
              ),
              child: todo.isCompleted
                  ? const Icon(
                      Icons.check,
                      color: Colors.white,
                      size: 16,
                    )
                  : null,
            ),
          ),
          const SizedBox(width: 12),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  todo.title,
                  style: TextStyle(
                    fontSize: 14,
                    fontWeight: FontWeight.w500,
                    color: todo.isCompleted ? Colors.grey.shade500 : Colors.black87,
                    decoration: todo.isCompleted ? TextDecoration.lineThrough : null,
                  ),
                ),
                const SizedBox(height: 4),
                Container(
                  padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 2),
                  decoration: BoxDecoration(
                    color: priorityColor.withOpacity(0.1),
                    borderRadius: BorderRadius.circular(8),
                  ),
                  child: Text(
                    todo.priority == 'high' ? 'ÎÜíÏùå' : todo.priority == 'medium' ? 'Î≥¥ÌÜµ' : 'ÎÇÆÏùå',
                    style: TextStyle(
                      fontSize: 10,
                      color: priorityColor,
                      fontWeight: FontWeight.bold,
                    ),
                  ),
                ),
              ],
            ),
          ),
          IconButton(
            onPressed: () => _deleteTodo(index),
            icon: Icon(
              Icons.delete_outline,
              color: Colors.red.shade400,
              size: 20,
            ),
            tooltip: 'ÏÇ≠Ï†ú',
          ),
        ],
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    int completedCount = _todos.where((todo) => todo.isCompleted).length;
    double completionRate = _todos.isEmpty ? 0 : completedCount / _todos.length;
    
    // Ìï†Ïùº Îç∞Ïù¥ÌÑ∞Î•º Map ÌòïÏãùÏúºÎ°ú Î≥ÄÌôò
    List<Map<String, dynamic>> todosData = _todos.map((todo) => {
      'title': todo.title,
      'isCompleted': todo.isCompleted,
      'priority': todo.priority,
    }).toList();

    return Scaffold(
      backgroundColor: Colors.pink.shade50,
      appBar: AppBar(
        title: const Text(
          'üå∏ ThinQ Ìôà',
          style: TextStyle(fontWeight: FontWeight.bold),
        ),
        backgroundColor: Colors.pink.shade400,
        foregroundColor: Colors.white,
        elevation: 0,
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(20),
        child: Column(
          children: [
            // Ï∫êÎ¶≠ÌÑ∞ Ïù¥ÎØ∏ÏßÄ
            _buildCharacterImage(),
            const SizedBox(height: 25),
            
            // Ïò§ÎäòÏùò ÏÑ±Í≥º
            _buildQuickStats(),
            const SizedBox(height: 25),
            
            // Ìï†Ïùº Í¥ÄÎ¶¨
            _buildTodoManagement(),
            const SizedBox(height: 25),
            
            // Î°úÏª¨ ML ÏúÑÏ†Ø
            LocalMLWidget(
              todos: todosData,
              completionRate: completionRate,
              totalTodos: _todos.length,
              completedTodos: completedCount,
              studyTimeMinutes: 60, // Í∏∞Î≥∏Í∞í
              currentMood: completionRate > 0.7 ? 'happy' : completionRate > 0.4 ? 'working' : 'encouraging',
            ),
          ],
        ),
      ),
    );
  }
}

class TodoItem {
  final String title;
  final bool isCompleted;
  final String priority; // high, medium, low
  
  TodoItem({
    required this.title, 
    required this.isCompleted,
    this.priority = 'medium',
  });
} 

